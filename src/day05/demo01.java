package day05;

public class demo01 {
    //main是主方法，一个类只能一个
    public static void main(String[] args) {
        //数字不需要放在双引号""中
        System.out.println(2);
        /**
         * 所有字符串（中文 符号 英文  ）
         * 必须放在双引号当中
         */
        System.out.println("我每天都在认真学java");

        //lb代表换行，在双引号中用\n可以实现
        System.out.println(242);
        System.out.println("第一行\n第二行\n第三行");

        /**
         * 在以后的java当中 用不上\n\t
         * 现在知识学习编程思路上有用
         */
    }
}
